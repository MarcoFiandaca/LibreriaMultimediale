    public VisualizzaInformazioni(MyLibreria lib, int pos, MyTableModel modello) {
        setLocationRelativeTo(null);
        this.lib=lib;
        this.pos=pos;
        this.elemento=lib.get(pos);
        this.modelloTabella=modello;
        
        initComponents();
        jLabelFrequenza.setVisible(false);
        jTextFrequenza.setVisible(false);
        checkboxSalva.setEnabled(false);
       
        
        switch (elemento.getTipo()){
            case "MP3": 
                jLabelFrequenza.setVisible(true);
                jTextFrequenza.setVisible(true);
                jLabelA.setText("Durata"); 
                BranoMusicale BN = (BranoMusicale)elemento; 
                JTextA.setText(BN.getDurata());
                jTextFrequenza.setText(BN.getFrequenza());       
                break;
                
            case "MP4": jLabelA.setText("Durata"); Video V = (Video)elemento; JTextA.setText(V.getDurata()); break;
            case "PDF": jLabelA.setText("NÂ°Pagine"); EBook E = (EBook)elemento; JTextA.setText(E.getNPagine()); break;
        }
        JTextTitolo.setText(elemento.getTitolo());
        JTextAutore.setText(elemento.getAutore());
        JTextPercorso.setText(elemento.getPercorso());
        JTextAnno.setText(elemento.getAnno());
        this.setVisible(true);
        
    }

